    public void preencherTabelaUsuario() {

        try {
            String[] nomeColunas = new String[]{"Sexo", "Faixa Etária"};
            ArrayList dados = controlePrincipal.listarUsuarios(dao);
            DefaultTableCellRenderer centralizado = new DefaultTableCellRenderer();
            centralizado.setHorizontalAlignment(SwingConstants.CENTER);
            ModeloTabela modeloTabela = new ModeloTabela(dados, nomeColunas);
            jTableListarUsuario.setModel(modeloTabela);

            jTableListarUsuario.getColumnModel().getColumn(0).setPreferredWidth(80);
            jTableListarUsuario.getColumnModel().getColumn(0).setResizable(true);
            jTableListarUsuario.getColumnModel().getColumn(0).setHeaderValue(modeloTabela.getColumnNome(0));
            jTableListarUsuario.getColumnModel().getColumn(0).setCellRenderer(centralizado);

            jTableListarUsuario.getColumnModel().getColumn(1).setPreferredWidth(150);
            jTableListarUsuario.getColumnModel().getColumn(1).setResizable(true);
            jTableListarUsuario.getColumnModel().getColumn(1).setHeaderValue(modeloTabela.getColumnNome(1));
            jTableListarUsuario.getColumnModel().getColumn(1).setCellRenderer(centralizado);

            jTableListarUsuario.getTableHeader().setReorderingAllowed(false);
            jTableListarUsuario.setAutoResizeMode(JTable.AUTO_RESIZE_ALL_COLUMNS);
            jTableListarUsuario.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        } catch (Exception exc) {
            JOptionPane.showMessageDialog(null, "Erro ao listar os usuários cadastrados!");
        }
    }

