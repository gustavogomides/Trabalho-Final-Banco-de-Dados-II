    private void preencherComboBoxs() {

        assuntos = controlePrincipal.assuntos(dao);
        for (String s : assuntos) {
            jComboBoxAssunto.addItem(s + "\n");
        }

        comocomproucont = controlePrincipal.comoComprouContratou(dao);
        for (String s : comocomproucont) {
            jComboBoxComoComprouContratou.addItem(s + "\n");
        }

        grupoproblem = controlePrincipal.grupoProblema(dao);
        for (String s : grupoproblem) {
            jComboBoxGrupoProblema.addItem(s + "\n");
        }

        problem = controlePrincipal.problema(dao);
        for (String s : problem) {
            jComboBoxProblema.addItem(s + "\n");
        }

        city = controlePrincipal.cidade(dao);
        for (String s : city) {
            jComboBoxCidade.addItem(s + "\n");
        }
    }

