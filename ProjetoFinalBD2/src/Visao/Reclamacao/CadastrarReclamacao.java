    private void jButtonCadastrarReclamacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarReclamacaoActionPerformed

        if (anoabertura == 0) {
            anoabertura = 2014;
        }
        if (assunto == null) {
            assunto = assuntos.get(0);
        }
        if (avaliacaoreclamacao == null) {
            avaliacaoreclamacao = "Resolvida";
        }
        if (comocomproucontratou == null) {
            comocomproucontratou = comocomproucont.get(0);
        }
        if (grupoproblema == null) {
            grupoproblema = grupoproblem.get(0);
        }
        if (mesabertura == 0) {
            mesabertura = 1;
        }
        if (problema == null) {
            problema = problem.get(0);
        }
        if (procurouempresa == null) {
            procurouempresa = "Sim";
        }
        if (respondida == null) {
            respondida = "Sim";
        }
        if (situacao == null) {
            situacao = "Finalizada avaliada";
        }

        if (regiao == null) {
            regiao = "CO";
        }

        if (uf == null) {
            uf = "AC";
        }

        if (cidade == null) {
            cidade = city.get(0);
        }
        if (jComboBoxTempoResposta.getSelectedItem().toString() == null) {
            temporesposta = 0;
        }

        if (this.jDateChooserAbertura.getDate() != null) {
            dataabertura = this.jDateChooserAbertura.getDate();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma data de abertura!", "Data Abertura", JOptionPane.WARNING_MESSAGE);
        }

        if (this.jDateChooserResposta.getDate() != null) {
            dataresposta = this.jDateChooserResposta.getDate();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma data de resposta!", "Data Resposta", JOptionPane.WARNING_MESSAGE);
        }

        if (this.jDateChooserFinalizacao.getDate() != null) {
            datafinalizacao = this.jDateChooserFinalizacao.getDate();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma data de finalização!", "Data Finalização", JOptionPane.WARNING_MESSAGE);
        }

        if (nota == -1) {
            JOptionPane.showMessageDialog(null, "Selecione uma nota para a reclamação!", "Nota Consumidor", JOptionPane.WARNING_MESSAGE);
        } else if (!usuario && empresa) {
            JOptionPane.showMessageDialog(null, "Selecione um usuário!", "Informação", JOptionPane.WARNING_MESSAGE);
        } else if (!empresa && usuario) {
            JOptionPane.showMessageDialog(null, "Selecione uma empresa!", "Informação", JOptionPane.WARNING_MESSAGE);
        } else if (!empresa && !usuario) {
            JOptionPane.showMessageDialog(null, "Selecione um usuário e uma empresa!", "Informação", JOptionPane.WARNING_MESSAGE);
        } else {
            java.sql.Date dataAberturaSQL = new java.sql.Date(dataabertura.getTime());
            java.sql.Date dataRespostaSQL = new java.sql.Date(dataresposta.getTime());
            java.sql.Date dataFinalizacaoSQL = new java.sql.Date(datafinalizacao.getTime());

            SimpleDateFormat simpleDateFormat = new SimpleDateFormat("dd/MM/yyyy");
            String s
                    = "Reclamação a ser inserida:"
                    + "\n\nRegião: " + regiao
                    + "\nUF: " + uf
                    + "\nCidade: " + cidade
                    + "\n\nAssunto: " + assunto
                    + "\nMes Abertura: " + mesabertura
                    + "\nAno abertura: " + anoabertura
                    + "\nData Abertura: " + simpleDateFormat.format(dataabertura)
                    + "\nNota Consumidor: " + nota
                    + "\nComo Comprou Contratou: " + comocomproucontratou
                    + "\nGrupo Problema: " + grupoproblema
                    + "\nProcurou Emprea: " + procurouempresa
                    + "\nTempo Resposta: " + temporesposta
                    + "\nAvaliação Reclamação: " + avaliacaoreclamacao
                    + "\nSituação: " + situacao
                    + "\nRespondida: " + respondida
                    + "\nData Finalização: " + simpleDateFormat.format(datafinalizacao)
                    + "\nData Resposta: " + simpleDateFormat.format(dataresposta)
                    + "\nProblema: " + problema
                    + "\n\nSexo Usuário: " + sexo
                    + "\nFaixa Etária Usuário: " + faixaetaria
                    + "\n\nNome Empresa: " + nomefantasia
                    + "\nÁrea: " + area
                    + "\nSegmento Mercado: " + segmentomercado
                    + "\n\nDeseja inserir esta reclamação?";

            int opcao = JOptionPane.showConfirmDialog(null, s, "Inserir Reclamação", JOptionPane.YES_NO_OPTION);

            if (opcao == JOptionPane.YES_OPTION) {
                if (controlePrincipal.cadastrarReclamacao(regiao, uf, cidade, assunto, mesabertura,
                        dataAberturaSQL,
                        nota, anoabertura, comocomproucontratou, grupoproblema, procurouempresa,
                        temporesposta, avaliacaoreclamacao, situacao, respondida, dataFinalizacaoSQL,
                        dataRespostaSQL, problema, dao, nomefantasia, area, segmentomercado, sexo,
                        faixaetaria)) {

                    JOptionPane.showMessageDialog(null, "Reclamação cadastrada com sucesso!");
                } else {
                    JOptionPane.showMessageDialog(null, "Reclamação não foi cadastrada!", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            }

        }
    }//GEN-LAST:event_jButtonCadastrarReclamacaoActionPerformed

